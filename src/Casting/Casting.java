package Casting;

public class Casting {
	
	
	public static void main(String[] args) {
		
		
		long i = 10; 
		//byte b, short s, int i, long l, float f, double d 
		
		byte b = (byte) i; 
		
		short s = (short) i; 
		
		int j = (int) i; 
		
		long l = i; 
		
		float k = i; 
		
		double m = i; 
		
		float f = (float) i; 
		
		double d = (double) i; 
		
		
		
		
	}
	

}
