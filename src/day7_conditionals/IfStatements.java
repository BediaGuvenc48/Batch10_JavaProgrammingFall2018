package day7_conditionals;

public class IfStatements {
	
	public static void main(String[] args) {
		
		if (10 > 5) {
			
			System.out.println("It is true");
		}
		int chairs = 55; 
		
		if (chairs == 75) {
			System.out.println( "We have 75 chairs");
		}
		if (chairs > 75) {
		System.out.println( "We don't have that many chairs");
		}
		
		if (chairs < 75) { 
			
			System.out.println("We have more chairs if you need");
		}
	}

}
